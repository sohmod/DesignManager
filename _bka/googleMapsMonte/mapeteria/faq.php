    <a name="legend"></a>
    <b>Why don't you have a legend on your maps?</b><p />
    I'm hugely ambivalent about legends.  On the one hand, people clearly
    want them.  On the other hand, I am uncomfortable with people trying to
    extract quantitative data from these maps.  If you want to know what
    the actual numeric values are, you really should be looking at a table.
    This kind of thematic map is most useful for comparisons along the lines
    of "California's population is bigger than Vermont's" or "the median body mass
    index is higher in Texas than in Oregon".
    <p />
   
